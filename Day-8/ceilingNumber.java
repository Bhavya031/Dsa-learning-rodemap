    static int ceiling(int[] arr,int numb){
        int start=0;
        int end=arr.length-1;
        if (numb > arr[arr.length - 1]) {
            return -1;
        }
        while (start<=end){
            int mid=start+(end-start)/2;


            if (numb < arr[mid]){
                end=mid-1;
            } else if (numb>arr[mid]){
                start = mid + 1;
            }
            else {
                return mid;
            }
            }
        return start;
    }
